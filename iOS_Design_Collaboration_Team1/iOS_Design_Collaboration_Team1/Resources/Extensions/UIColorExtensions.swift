import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var black: UIColor {
        return UIColor(white: 26.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var inactiveGrey: UIColor {
        return UIColor(white: 203.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var pink: UIColor {
        return UIColor(red: 236.0 / 255.0, green: 93.0 / 255.0, blue: 119.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var backgroundWhite: UIColor {
        return UIColor(red: 248.0 / 255.0, green: 246.0 / 255.0, blue: 250.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var green: UIColor {
        return UIColor(red: 22.0 / 255.0, green: 193.0 / 255.0, blue: 155.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var mainBlue: UIColor {
        return UIColor(red: 40.0 / 255.0, green: 120.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var white80: UIColor {
        return UIColor(white: 1.0, alpha: 0.8)
    }
    @nonobjc class var paleGrey: UIColor {
        return UIColor(red: 251.0 / 255.0, green: 251.0 / 255.0, blue: 254.0 / 255.0, alpha: 1.0)
      }
    @nonobjc class var blackTwo: UIColor {
        return UIColor(white: 48.0 / 255.0, alpha: 1.0)
      }


}
